export const blockAtlasByKey = Object.fromEntries(blockAtlas.map((entry) => [entry.key, entry]));

export function getBlockAtlasEntry(key) {
  return blockAtlasByKey[key] ?? null;
}

function block(entry) {
  const dimensionsM = entry.dimensionsM ?? resourceBlockDefaultDimensionsM;
  const densityKgM3 = entry.densityKgM3 ?? blockDensityKgM3[entry.key] ?? null;
  return {
    rarityLabel: rarityLabel(entry.signals.rarity),
    physical: physicalProfileFromDensity(densityKgM3, dimensionsM),
    ...entry,
  };
}

function rarityLabel(value) {
  if (value >= 78) return "Anomaly";
  if (value >= 68) return "Rare";
  if (value >= 50) return "Uncommon";
  return "Common";
}
